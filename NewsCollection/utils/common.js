//获取新闻列表
function getNewsList() {
  let list = [];
  for (let a = 0; a < news.length; a++) {
    let obj = {}
    obj.id = news[a].id
    obj.title = news[a].title
    obj.poster = news[a].poster

    obj.date = news[a].date
    list.push(obj)
  }
  return list;
}

function getNewsDetail(newId){
  let msg={
    code:"404",
    news:{}
  }
  for (let a = 0; a < news.length; a++) {
    if(news[a].id===newId){
      msg.code="200"
      msg.news=news[a]
      break;
    }
  }
  return  msg;
}
module.exports={
  getNewsDetail,
  getNewsList
}